a = 100
b = 200

# Comparison Always give answer in boolean

# ' == ' compare booth side equals or not
print(a == b)

# '!=' not equal to both side are not same
print(a != b)

# ' > 'greater than
print(a > b)

# ' < ' less than
print(a < b)

# ' >= ' greater or equal
print(a >= b)

# ' <= ' less or equal
print(a <= b)
