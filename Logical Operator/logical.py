# AND - OR - NOT
print(12 < 13 and 34 > 23)  # if 1 condition is false,it will give answer false. if both conditions are true, it is true
print(12 < 13 or 34 > 23)  # if 1 condition it true it will give true
print(not 12 < 13)  # not converts result opposite it answer is true it will give false, vice versa!
print(not True)
print(not False)
print(not 12 > 13 and 34 > 12 and not 67 < 34)
