# type conversion
a = "12"
print(type(a))

# conversion from sting to integer
a = int(a)
print(a + 12)

# conversion from string to float
b = float(a)
print(b)

# conversion from int to string
c = 123
d = str(c)
print(d + '123')

# false values - False, None, Zero Numeric Values, Empty Sequence & Collections
e = "1234"
f = bool(e)
print(f)
